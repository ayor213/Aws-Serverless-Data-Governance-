    "\n",
    "# Date range\n",
    "start_date = \"2018-03-01\"\n",
    "end_date   = \"2026-01-01\"\n",
    "\n",
    "# List to hold all DataFrames\n",
    "all_data = []\n",
    "\n",
    "for ticker in tickers:\n",
    "    print(f\"Fetching {ticker}...\")\n",
    "    data = yf.download(ticker, start=start_date, end=end_date)\n",
    "    if not data.empty:\n",
    "        data[\"Ticker\"] = ticker  # Add Ticker column\n",
    "        all_data.append(data.reset_index())\n",
    "\n",
    "# Combine all stocks into a single DataFrame\n",
    "combined_df = pd.concat(all_data, ignore_index=True)\n",
    "\n",
    "# Convert to CSV string\n",
    "csv_buffer = StringIO()\n",
    "combined_df.to_csv(csv_buffer, index=False)\n",
    "\n",
    "# Upload to S3\n",
    "object_key = \"combined_daily_prices.csv\"\n",
    "s3.put_object(Bucket=bucket_name, Key=object_key, Body=csv_buffer.getvalue())\n",
