    "s3 = boto3.client(\"s3\", region_name=\"us-east-1\")\n",
    "\n",
    "# 30 stocks\n",
    "tickers = [\n",
    "    \"AAPL\", \"MSFT\", \"GOOGL\", \"AMZN\", \"FB\",\n",
    "    \"TSLA\", \"NVDA\", \"BRK-B\", \"JPM\", \"JNJ\",\n",
